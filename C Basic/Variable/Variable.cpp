#include <stdio.h>

int main()
{
    int num1 = 5;
    float num2 = 1.5f;
    double num3 = 3.141592;
    char a = 'a';

    printf("%d\n", num1);  // int 자료형 -> %d
    printf("%f\n", num2);  // float 자료형 -> %f
    printf("%lf\n", num3); // double 자료형 -> %lf
    printf("%c\n", a);     // char 자료형 -> %c
    
}


